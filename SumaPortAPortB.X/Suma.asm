    LIST P=18F4550
    #INCLUDE<p18f4550.inc>
    
    #DEFINE D1 H'05'
    #DEFINE SUMA H'09'
    
    ORG 0
    
    ;PROGRAMA
    
    CONFIGURAR
	MOVLW B'00000011'
	MOVWF TRISA, 0
	MOVLW H'07'
	MOVWF CMCON, 0
	MOVLW H'0F'
	MOVWF ADCON1, 0
	CLRF PORTA, 0
	CLRF TRISB, 0
	CLRF PORTB, 0
	
    MAINSUMA
	MOVLW H'07'
	MOVWF D1, 1
	MOVF PORTA, W, 0
	ADDWF D1, 0, 1
	MOVFF WREG, SUMA
	MOVWF PORTB, 0
	GOTO MAINSUMA
	END