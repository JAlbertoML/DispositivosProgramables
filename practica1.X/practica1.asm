; PROGRAMA QUE HACE LAS SIGUIENTES OPERACIONES:
    ; SUMAR 200 + 60
    ; RESTAR 200 - 60
    ; RESTAR 1 - 130
    ; MULTIPLICAR 40 * 56 Y GUARDAR EL RESULTADO EN LOS REGISTROS 20 Y 21 DEL BANCO 4
    
; DISPOSITIVO A UTILIZAR
    LIST P = 18F4550
    
; LIBRERÍAS
    #INCLUDE <p18f4550.inc>
    
; ASIGNAR ETIQUETAS A LOS REGISTROS
    RS	    EQU	    0X09    ; RESULTADO DE LA SUMA EN REGISTRO 9
    RR1	    EQU	    0X0A    ; RESULTADO DE LA RESTA 1 EN REGISTRO 10    
    RR2	    EQU	    0X0B    ; RESULTADO DE LA RESTA 2 EN REGISTRO 11
    RML	    EQU	    0X14    ; RESULTADO DE LA MULTIPLICACIÓN L EN REGISTRO 12
    RMH	    EQU	    0X15    ; RESULTADO DE LA MULTIPLICACION H EN REGISTRO 13
	    
; ASIGNAR ETIQUETAS A LOS VALORES CONSTANTES
    _200    EQU	    0XC8
    _1	    EQU	    0X01
    _60	    EQU	    0X3C
    _130    EQU	    0X82
    _40	    EQU	    0X28
    _56	    EQU	    0X38

; ORIGEN DEL PROGRAMA
    ORIGEN  ORG	    0X00
  
; PROGRAMA
    SUMA
	MOVLB	0X00	; CAMBIAR AL BANCO 0
	MOVLW	_200	; W = 200
	ADDLW	_60	; W = W + 60 -> W = 260
	MOVWF	RS, 1	; RS = W
	
    RESTA1
	MOVLW	_60	; W = 60
	SUBLW	_200	; W = 200 - W
	MOVWF	RR1, 1	; RR1 = W
	
    RESTA2
	MOVLW	_130	; W = 130
	SUBLW	_1	; W = 1 - W
	MOVWF	RR2, 1	; RR2 = W
	
    MULTIPLICACION
	MOVLB	0X04	; CAMBIO AL BANCO 4
	MOVLW	_40	; W = 40
	MULLW	_56	; W = 56 * W
	MOVLW	PRODH	; W = PRODH
	MOVWF	RMH, 1	; RMH = W
	MOVLW	PRODL	; W = PRODL
	MOVWF	RML, 1	; RML = W
	
	GOTO SUMA
	END